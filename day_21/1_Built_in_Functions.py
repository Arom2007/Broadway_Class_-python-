# All those functions which are already defined in python are Python built-in functions.

print("Hello World")

len([1, 2, 3, 4, 5])  # Gives the number of items in the list

sum([1, 2, 3, 4, 5])  # Adds the items in the list

max([1, 2, 3, 4, 5])  # Gives the largest number

min([1, 2, 3, 4, 5])  # Gives the smallest number

sorted([2, 1, 5, 3, 5])  # Sorts the list in ascending order

sorted([2, 1, 5, 3, 5], reverse=True)  # Sorts the list in descending order

data = reversed([1, 2, 3, 4, 5])  # Gives the list in reverse order

